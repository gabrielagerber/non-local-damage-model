C
C     ROTATING THE EFFECTIVE STRESS AND THE DERIVATIVE OF KAPPA
C     W.R.T THE STRAIN TENSOR FROM THE MATERIAL BASIS INTO THE 
C     ELEMENT BASIS
C
      CALL KROT2(DKDE,XB,DKDER)
      DKDE = DKDER      
