      NNLTNNLDK=MATMUL(NNLTNNL,KAPPABARN)
      XLBNLTBNLDK=MATMUL(XLBNLTBNL,KAPPABARN)
      XKNNLT=KAPPA1*NNLT
      FNL1=NNLTNNLDK+XLBNLTBNLDK
      FNL=FNL1-XKNNLT
