      DO K1=1,30
      DO K3=1,6
      RHS(K1,1)=RHS(K1,1)-BT(K1,K3)*STRESS(K3)*DETJ2*W(IP)/SIX
      END DO
      END DO    
C      
      DO K1=1,4
      RHS(K1+30,1)=RHS(K1+30,1)-FNL(K1,1)*DETJ2*W(IP)/SIX
      END DO      
C      
      END DO    
      
C      IF (KINC .EQ. 50) THEN
