*****************************************************************
**        CALCULATE THE CONDITION NUMBER OF A NXN MATRIX        *
*****************************************************************
**
*USER SUBROUTINE
      SUBROUTINE KCONDNO(A,N,C)
C     
C     The condition number of a matrix measures the sensitivity of 
C     the solution of a system of linear equations to errors in the data.  
C
      INCLUDE 'ABA_PARAM.INC'
C
      DOUBLE PRECISION A(N,N), B(N,N)
      DOUBLE PRECISION C, X, Y
      INTEGER N
C 
      CALL MIGS(A,N,B)
C 
C     B IS THE INVERSE OF A
C
      X=0.D0
      Y=0.D0
C 
C     X AND Y ARE EUCLEADIAN NORMS OF A AND B RESPECTIVELY
C
      DO I=1,N
      DO J=1,N
       X=X+A(I,J)*A(I,J)
       Y=Y+B(I,J)*B(I,J)
      END DO
      END DO
C
      C=DSQRT(X)*DSQRT(Y)
C
      RETURN
      END
