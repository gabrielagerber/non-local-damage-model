C
C    SDV 1-6: TOTAL STRESS TENSOR 
C    SDV 7: CUMULATED PLASTIC STRAIN
C    SDV 8: SCALAR DAMAGAE VARIABLE
C    SDV 9-14: PLASTIC STRAIN VECTOR
