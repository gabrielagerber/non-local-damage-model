C     PROPS(3) - MM1
C     PROPS(4) - MM2
C     PROPS(5) - MM3
C     PROPS(6) - DENSIFICATOR FLAG: 0 - DENSIFICATION OFF
C                                   1 - DENSIFICATION ON
C     PROPS(7) - VISCOSITY FLAG:    0 - RATE-INDEPENDENT
C                                   1 - LINEAR VISCOSITY
C                                   2 - EXPONENTIAL VISC
C                                   3 - LOGARITHMIC VISC
C                                   4 - POLYNOMIAL VISC
C                                   5 - POWER LAW VISC 
C     PROPS(8) - POSTYIELD FLAG:    0 - PERFECT PLASTICITY
C                                   1 - LINEAR HARDENING
C                                   2 - EXPONENTIAL HARDENING
C                                   3 - QUADRATIC HARDENING
C     PROPS(9) - XM:                NONLOCAL WEIGHT
